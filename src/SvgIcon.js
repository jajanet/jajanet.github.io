// Get an array of length len that go up to a max of upper
function getRandomUniqueNumbers(upper, len) {
  const result = [];
  for (let i = 0; i < len; i++) {
      let randomNumber;
      do {
          randomNumber = Math.floor(Math.random() * upper);
      } while (result.includes(randomNumber));
      result.push(randomNumber);
  }
  return result;
}

